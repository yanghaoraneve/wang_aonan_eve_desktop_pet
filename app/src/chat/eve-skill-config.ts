export function upgradeSystemPrompt(value: unknown): string {
  const prompt = typeof value === "string" ? value.trim() : "";
  if (!prompt) return EVE_SKILL_SYSTEM_PROMPT;
  if (LEGACY_SYSTEM_PROMPTS.some((legacy) => normalize(legacy) === normalize(prompt))) {
    return EVE_SKILL_SYSTEM_PROMPT;
  }
  return prompt;
}

function normalize(value: string): string {
  return value.replace(/\s+/g, "\n").trim();
}
